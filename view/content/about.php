  <h1><font style="color: #f85351;">#</font> About</h1>
  <hr>
  <p>Lupi is a Universal API that builds over Laravel microservices (lumen). The API on this Lupi is reusable, and it seems like a starter API that you can modify as you want. Especially if you are familiar with Laravel, it's easy to read and modified Lupi. Lupi will cover almost all about the backend process and make the front-end more the fastest on the development process. The API in Lupi is dynamic, you can change parameters as you want and get the result. Let's try Lupi and share your experience</p>

  <p>Foundation of Lupi is Lumen, it means all of Laravel syntax and function can be used to modify this API. You can using all of <a href="https://laravel.com/docs/7.x">Laravel </a> or <a href="https://lumen.laravel.com/docs/6.x/releases">Lumen</a> documentation over Lupi. Absolutely you must install composer and other requirements to running laravel well on your computer before using Lupi. Lumen choosing for a foundation that because lumen is lightwell than laravel and if you need other packages you can install as you need.</p>
